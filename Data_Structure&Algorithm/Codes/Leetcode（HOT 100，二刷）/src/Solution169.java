public class Solution169 {
    public int majorityElement(int[] nums) {
        int count = 0;
        // 多数元素的候选者
        Integer candidate = null;

        for (int num : nums) {
            if (count == 0) {
                candidate = num;
            }
            count += (num == candidate) ? 1 : -1;
        }

        return candidate;
    }
}
