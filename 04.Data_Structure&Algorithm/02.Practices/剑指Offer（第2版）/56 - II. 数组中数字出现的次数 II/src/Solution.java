public class Solution {
    public int singleNumber(int[] nums) {
        // 记录所有数字的各二进制位的 1 的出现次数
        int[] counts = new int[32];
        for (int num : nums) {
            for (int j = 0; j < 32; j++) {
                counts[j] += num & 1;
                num >>>= 1;
            }
        }
        // 将 counts 各元素对 3 求余，则结果为 “只出现一次的数字” 的各二进制位
        int res = 0, m = 3;
        for (int i = 0; i < 32; i++) {
            res <<= 1;
            res |= counts[31 - i] % m;
        }
        return res;
    }
}
